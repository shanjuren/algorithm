package com.algorithm.monotonicStack;

/**
 * @author ght
 * @date 2022.04.13 6:50 PM
 * @description
 */
public class ListNode {

    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
